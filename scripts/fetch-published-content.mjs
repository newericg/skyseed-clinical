#!/usr/bin/env node
/**
 * Fetches published articles from the Skyseed API and writes blog-published.generated.ts
 * Usage: SKYSEED_API_URL=https://skyseed.com.br BUILD_API_KEY=secret node scripts/fetch-published-content.mjs
 */

import { writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const apiUrl = (process.env.SKYSEED_API_URL ?? 'http://localhost:8787').replace(/\/$/, '');
const buildKey = process.env.BUILD_API_KEY ?? '';

const outPath = resolve('src/app/data/blog-published.generated.ts');

async function main() {
  if (!buildKey) {
    console.warn('[fetch-published-content] BUILD_API_KEY not set — writing empty generated file.');
    writeEmpty();
    return;
  }

  const response = await fetch(`${apiUrl}/api/articles/published`, {
    headers: { 'x-build-key': buildKey },
  });

  if (!response.ok) {
    throw new Error(`Failed to fetch published articles: ${response.status} ${response.statusText}`);
  }

  const data = await response.json();
  const articles = data.articles ?? [];

  const meta = articles.map((article) => ({
    slug: article.slug,
    title: article.title,
    excerpt: article.excerpt,
    metaDescription: article.metaDescription,
    publishedAt: article.publishedAt ?? article.updatedAt,
    coverImage: article.coverImage,
  }));

  const sections = Object.fromEntries(
    articles.map((article) => [article.slug, article.sections]),
  );

  const slugs = articles.map((article) => article.slug);

  const content = `// AUTO-GENERATED by scripts/fetch-published-content.mjs — do not edit manually
import type { BlogArticleMeta } from './blog-articles-meta';
import type { ArticleSections } from './blog-articles-content';

export const PUBLISHED_BLOG_META: BlogArticleMeta[] = ${JSON.stringify(meta, null, 2)};

export const PUBLISHED_ARTICLE_SECTIONS: Record<string, ArticleSections> = ${JSON.stringify(sections, null, 2)};

export const PUBLISHED_BLOG_SLUGS: string[] = ${JSON.stringify(slugs, null, 2)};
`;

  writeFileSync(outPath, content, 'utf8');
  console.log(`[fetch-published-content] Wrote ${articles.length} published article(s).`);
}

function writeEmpty() {
  const content = `// AUTO-GENERATED by scripts/fetch-published-content.mjs — do not edit manually
import type { BlogArticleMeta } from './blog-articles-meta';
import type { ArticleSections } from './blog-articles-content';

export const PUBLISHED_BLOG_META: BlogArticleMeta[] = [];
export const PUBLISHED_ARTICLE_SECTIONS: Record<string, ArticleSections> = {};
export const PUBLISHED_BLOG_SLUGS: string[] = [];
`;
  writeFileSync(outPath, content, 'utf8');
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
